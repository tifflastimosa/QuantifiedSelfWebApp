package com.project.quantifiedself.repositories.activityweb;

/**
 * ActivityYear class that gets an activity for a given year.
 */
public class ActivityYear {

    public String activity;
    public String year;

    /**
     * Method gets the activity.
     *
     * @return The name of the activity.
     */
    public String getActivity() {
        return activity;
    }

    /**
     * Method gets year of the activity.
     *
     * @return The year of the activity.
     */
    public String getYear() {
        return year;
    }

}
